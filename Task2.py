#Using math module for calculations.
import math

num = int(input("Enter a number: "))
def square_root():
    return math.sqrt(num)
print(f"Square root: {square_root()}")
def Logarithm():
    return math.log(num, 2)
print(f"Logarithm: {Logarithm()}")
def Sine():
    return math.sin(num)
print(f"Sine: {Sine()}")
